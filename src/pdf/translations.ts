// Função para obter tradução
export const getPDFTranslation = (language: string = 'pt', key: string, subKey?: string, subSubKey?: string): string => {
  const translations = PDF_TRANSLATIONS[language as keyof typeof PDF_TRANSLATIONS] || PDF_TRANSLATIONS.pt
  
  if (subSubKey) {
    return (translations as any)[key]?.[subKey]?.[subSubKey] || key
  }
  
  if (subKey) {
    return (translations as any)[key]?.[subKey] || key
  }
  
  return (translations as any)[key] || key
}
